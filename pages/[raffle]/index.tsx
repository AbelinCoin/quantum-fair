import Head from "next/head";
import { useState, useEffect } from "react";
import { useRouter } from "next/router";
import { ethers } from "ethers";
import Navbar from "../../components/nav";
import { FairHub } from "../../components/abis";
import {
  Flex,
  CardSearch,
  NftCardInt,
  NftCard,
  NftImg,
} from "../../components/styles/div";
import { RffId } from "../../components/styles/typography";

function Heading() {
  return (
    <>
      <Head>
        <title>Raffle | Quantum Fair</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.png" />
      </Head>
    </>
  );
}

function Search() {
  const src =
    "https://img.freepik.com/vector-gratis/vector-tipografia-estilo-basura-trazo-pincel-signo-interrogacion_53876-140880.jpg?w=360&t=st=1679373690~exp=1679374290~hmac=390ba5565909c8535c3822f8b922e80391f22ce0fa16cec820570232f777943f";
  const [exist, setExist] = useState(false);
  const [image, setImage] = useState(false);
  const { query } = useRouter();
  async function Raffle() {
    try {
      const id = query.id;
      const FairContract = "";
      const ethereum = (window as any).ethereum;
      const accounts = await ethereum.request({
        method: "eth_requestAccounts",
      });
      const provider = new ethers.providers.Web3Provider(ethereum);
      const walletAddress = accounts[0];
      const signer = provider.getSigner(walletAddress);
      const FairProxy = new ethers.Contract(FairContract, FairHub, signer);
      const search = await FairProxy.ids(id);
    } catch (err) {
      console.error(err);
    }
  }

  useEffect(() => {
    Raffle();
  });

  return (
    <>
      <Heading />
      <Navbar />
      <Flex>
        {" "}
        {exist ? (
          <CardSearch>
            {image ? (
              <NftCard>
                <NftImg />
              </NftCard>
            ) : (
              <NftCardInt>
                <NftImg src={src} />
              </NftCardInt>
            )}
            <RffId>Raffle #{query.id}</RffId>
          </CardSearch>
        ) : (
          <CardSearch>
            <NftCard>
              <NftImg src={src} />
            </NftCard>
            <RffId>Raffle #{query.id}</RffId>
          </CardSearch>
        )}
      </Flex>
    </>
  );
}

export default Search;
